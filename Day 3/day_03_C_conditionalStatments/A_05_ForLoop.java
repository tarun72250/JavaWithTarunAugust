package day_03_C_conditionalStatments;

public class A_05_ForLoop {

	public static void main(String[] args) {

		//for(initilization , condition , updataion){}
		for(int i=0 ; i <= 10 ; i++)
		{
			System.out.println(i);
		}
		

		for(char i='a' ; i<='z' ; i++)
		{
			System.out.println(i);
		}
		
		char num ='A';
		
		while(num <='Z')
		{
			System.out.println(num);//
			num++;
		}
	}

}
