package classAndObject;

public class A_05_Math {

	
	
	//instance member methods
	public void addition(int n , int m) {
		System.out.println("Addition of Two Integer : "+(n+m));
	}
	
	public void addition(int n , int m , int o) {
		System.out.println("Addition of Three Integer : "+(n+m+o));
	}
	
	public void addition(float a , float b) {
		System.out.println("Addition of Two Floats :"+(a+b));
	}
	
	
}
